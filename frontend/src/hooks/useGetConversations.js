/**
 * Custom hook to fetch conversations from the server.
 *
 * @returns {Object} An object containing the loading state and conversations array.
 * @property {boolean} loading - A boolean indicating whether the conversations are being loaded or not.
 * @property {Array} conversations - An array of conversation objects.
 */

import { useEffect, useState } from "react";
import toast from "react-hot-toast";

const useGetConversations = () => {
  const [loading, setLoading] = useState(false);
  const [conversations, setConversations] = useState([]);

  useEffect(() => {
    const getConversations = async () => {
      setLoading(true);
      try {
        const res = await fetch("/api/users");
        const data = await res.json();
        if (data.error) {
          throw new Error(data.error);
        }
        setConversations(data);
      } catch (error) {
        toast.error(error.message);
      } finally {
        setLoading(false);
      }
    };

    getConversations();
  }, []);

  return { loading, conversations };
};
export default useGetConversations;
